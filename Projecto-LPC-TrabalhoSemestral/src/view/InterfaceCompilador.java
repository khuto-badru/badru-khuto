    private void fButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fButton2ActionPerformed
        // Obtém o texto do JTextArea
        String codigo = jText_Source_Code.getText();

        // Cria um seletor de arquivos
        JFileChooser fileChooser = new JFileChooser();

        // Mostra a caixa de diálogo para salvar o arquivo
        int result = fileChooser.showSaveDialog(this);

        // Verifica se o usuário selecionou um local para salvar o arquivo
        if (result == JFileChooser.APPROVE_OPTION) {
            // Obtém o arquivo selecionado pelo usuário
            File arquivo = fileChooser.getSelectedFile();

            try {
                // Cria um BufferedWriter para escrever no arquivo
                BufferedWriter writer = new BufferedWriter(new FileWriter(arquivo));

                // Escreve o código no arquivo
                writer.write(codigo);

                // Fecha o BufferedWriter
                writer.close();

                // Exibe uma mensagem de sucesso
                JOptionPane.showMessageDialog(this, "Código salvo com sucesso!");

            } catch (IOException ex) {
                // Trata exceções de IO
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, "Erro ao salvar o código.");
            }
        }
    }//GEN-LAST:event_fButton2ActionPerformed

