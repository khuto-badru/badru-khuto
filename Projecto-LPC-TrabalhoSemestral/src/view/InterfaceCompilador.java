    private void btn_openActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_openActionPerformed
        // Cria uma instância do JFileChooser
        JFileChooser fileChooser = new JFileChooser();

        // Exibe o diálogo de seleção de arquivo
        int result = fileChooser.showOpenDialog(this);

        // Verifica se o usuário selecionou um arquivo
        if (result == JFileChooser.APPROVE_OPTION) {
            // Obtém o arquivo selecionado pelo usuário
            File selectedFile = fileChooser.getSelectedFile();

            try {
                // Cria um BufferedReader para ler o conteúdo do arquivo
                BufferedReader reader = new BufferedReader(new FileReader(selectedFile));

                // Cria uma StringBuilder para armazenar o conteúdo do arquivo
                StringBuilder content = new StringBuilder();
                String line;

                // Lê cada linha do arquivo e a adiciona à StringBuilder
                while ((line = reader.readLine()) != null) {
                    content.append(line).append("\n");
                }

                // Fecha o BufferedReader
                reader.close();

                // Obtém o conteúdo do arquivo como uma String
                String fileContent = content.toString();

                // Exibe o conteúdo do arquivo no JTextArea
                jText_Source_Code.setText(fileContent);
            } catch (IOException ex) {
                // Trata qualquer exceção de leitura do arquivo
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btn_openActionPerformed

